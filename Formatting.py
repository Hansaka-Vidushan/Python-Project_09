# Get pyformat.info

name = "Hansaka"
age = 19
na = "Hello %s. Your age is %d" %(name,age)

# We can use index for string formatting
ma = "Hello {1}. Your age is {0}".format(name,age)

# Using "f"
sa = f"Hello {name}. Your name is {age}"

print(na)
print(ma)
print(sa)


# String = %s
# int = %d
# float = %f




